# ==============================================================================
make_theta_row <- function(fit, label) {
  th <- fit$theta_hat
  data.table(
    sample           = label,
    n_obs            = fit$cache$n_obs,
    converged        = fit$converged,
    n_iter           = fit$n_iter,
    log_lik          = round(fit$log_likelihood, 1),

    kappa_exit       = round(th[["kappa_exit"]], 4),
    K                = round(th[["K"]],          4),
    gamma_price      = round(th[["gamma_price"]], 4),
    gamma_risk       = round(th[["gamma_risk"]],  4),

    kappa_exit_USD   = round(th[["kappa_exit"]] * SCALE_FACTOR, 0),
    K_USD            = round(th[["K"]]          * SCALE_FACTOR, 0)
  )
}

theta_tab <- rbindlist(list(
  make_theta_row(fit_observed,  "observed (TX 2006+, controls 1999+)"),
  make_theta_row(fit_extended,  "extended (TX 1999+ engine-imputed, controls 1999+)"),
  make_theta_row(fit_absorbing, "absorbing-only (Replace dropped, K_log pinned)")
))
print(theta_tab)
